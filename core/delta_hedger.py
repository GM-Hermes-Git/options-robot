"""
Дельта-хеджирование (DeltaHedger).

Поддерживает суммарную дельту позиции равной 0 путём открытия/закрытия
позиций во фьючерсе на базовый актив.

Версия для Этапа 1: ЗАГЛУШКА.
Полная реализация — Этап 4 (Хеджер и риски).
"""

import logging

logger = logging.getLogger(__name__)


class DeltaHedger:
    """
    Дельта-хеджер (заглушка для Этапа 1).

    Алгоритм (из ТЗ, раздел 6.2):
        - Пересчёт дельты при изменении цены БА на шаг пересчёта
        - Суммарная дельта = сумма дельт всех опционных позиций
        - Требуемый хедж = -суммарная дельта позиции
        - Если |требуемый хедж - текущий| >= 1 лот → ордер на корректировку
        - Цена хедж-ордера: Bid (продажа) / Ask (покупка)
        - Привязка фьючерсной позиции к стратегии через ID в комментарии
    """

    def __init__(self, event_bus: "EventBus", config: dict):  # noqa: F821
        self._event_bus = event_bus
        self._config = config
        logger.info("DeltaHedger: ЗАГЛУШКА (реализация — Этап 4)")
